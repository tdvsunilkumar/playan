$(document).ready(function(){	
	var yearpickerInput = $('input[name="revision_year"]').val();
      $('.yearpicker').yearpicker();
      $('.yearpicker').val(yearpickerInput).trigger('change');
      $('.yearpicker').yearpicker({dropdownAutoWidth : false,dropdownParent: $("#this_is_filter").css('display', 'block')});
	datatablefunction();
	$("#btn_search").click(function(){
 		datatablefunction();
 	});	
    $('#rptPropertySearchByText').on('keyup',function(){
        datatablefunction();
    });

    $('#rptPropertySearchByRevisionYear').on('change',function(e){
    	if($(this).val() != ''){
            $('.addNewPaymentScedule').attr('hidden',false);
        }else{
            $('.addNewPaymentScedule').attr('hidden',true);
        }
        datatablefunction();
    });
});



function datatablefunction()
{
	var dropdown_html=get_page_number('1'); 
	var table = $('#Jq_datatablelist').DataTable({ 
		"language": {
            "infoFiltered":"",
            "processing": "<img src='"+DIR+"public/images/ajax-loader1.gif' style='position: absolute;top: 50%;left: 50%;margin: -50px 0px 0px -50px;' />"
        },
        dom: "<'row'<'col-sm-12'f>>" +"<'row'<'col-sm-3'l><'col-sm-9'p>>" +"<'row'<'col-sm-12'tr>>" +"<'row'<'col-sm-12'p>>",
   
            oLanguage: {
	         	sLengthMenu: dropdown_html
        },
		"bProcessing": true,
		"serverSide": true,
		"bDestroy": true,
		"searching": false,
		"order": [],
		"columnDefs": [{ orderable: false, targets: [0,9] }],
		"pageLength": 10,
		"ajax":{ 
			url :DIR+'ctopaymentschedule/getList', // json datasource
			type: "GET", 
			"data": {
				"q":$("#rptPropertySearchByText").val(),
				"year":$("#rptPropertySearchByRevisionYear").val(),
                "_token":$("#_csrf_token").val()
		    }, 
			error: function(html){
			}
		},
       "columns": [
			{ "data": "srno" },
        	{ "data": "rcpsched_year" },
        	{ "data": "sd_mode" },
        	{ "data": "rcpsched_date_start" },
        	{ "data": "rcpsched_date_end" },
            { "data": "rcpsched_penalty_due_date" },
        	{ "data": "rcpsched_discount_due_date" },
        	{ "data": "rcpsched_discount_rate" },
			{ "data": "is_active" },
        	{ "data": "action" }
        ],
        drawCallback: function(s){ 
	        var api = this.api();
	        var info=table.page.info();
	        var dropdown_html=get_page_number(info.recordsTotal,info.length);
	         $("#common_pagesize").html(dropdown_html);
	        api.$('.deleterow').click(function() {
	            var recordid = $(this).attr('id');
	            DeleteRecord(recordid);
	        });
			api.$('.activeinactive').click(function() {
				var recordid = $(this).attr('id');
				var is_activeinactive = $(this).attr('value');
				 ActiveInactiveUpdate(recordid,is_activeinactive);
	
			});
	    }
	});  
}
function DeleteRecord(id){
	alert(id);
        const swalWithBootstrapButtons = Swal.mixin({
            customClass: {
                confirmButton: 'btn btn-success',
                cancelButton: 'btn btn-danger'
            },
            buttonsStyling: false
        })

        
        swalWithBootstrapButtons.fire({
            title: 'Are you sure?',
            text: "This action can not be undone. Do you want to continue?",
            icon: 'warning',
            showCancelButton: true,
            confirmButtonText: 'Yes',
            cancelButtonText: 'No',
            reverseButtons: true
        }).then((result) => {
            if(result.isConfirmed
            )
            {
               $.ajax({
			        url :DIR+'ctopaymentschedule/delete', // json datasource
			        type: "POST", 
			        data: {
			          "id": id, 
			          "_token": $("#_csrf_token").val(),
			        },
			        success: function(html){
			        	Swal.fire({
	    				  position: 'center',
	    				  icon: 'success',
	    				  title: 'Cto Payment Schedule Successfully.',
	    				  showConfirmButton: false,
	    				  timer: 1500
	    				})
			           location.reload();
			        }
			    })
            }
        })
}
